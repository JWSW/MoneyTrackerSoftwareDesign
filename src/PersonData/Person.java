package PersonData;

public class Person {
    private String name;
    private Integer schuld;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getSchuld() {
        return schuld;
    }

    public void setSchuld(Integer schuld) {
        this.schuld = schuld;
    }
}


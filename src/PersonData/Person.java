package PersonData;

public class Person {
    private String name;
    private double schuld;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getSchuld() {
        return schuld;
    }

    public void setSchuld(double schuld) {
        this.schuld = schuld;
    }
}

